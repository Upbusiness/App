    private void jTextParBuscaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextParBuscaKeyReleased
        if (jTextParBusca.getForeground().equals(new java.awt.Color(204, 204, 204))) {
            jTextParBusca.setForeground(new java.awt.Color(51, 51, 51));
        }
        if (jTextParBusca.getText().contains("LOCALIZAR PRODUTO POR CÓDIGO DESCRIÇÃO")) {
            if (jTextParBusca.getText().substring(0, jTextParBusca.getText().length() - 1).equals("LOCALIZAR PRODUTO POR CÓDIGO DESCRIÇÃO")) {
                jTextParBusca.setText(jTextParBusca.getText().substring(jTextParBusca.getText().length() - 1));
            }
        }
        if (jTextParBusca.getText().isEmpty()) {
            jTextParBusca.setText("LOCALIZAR PRODUTO POR CÓDIGO DESCRIÇÃO");
            jTextParBusca.setForeground(new java.awt.Color(204, 204, 204));
        }
    }//GEN-LAST:event_jTextParBuscaKeyReleased

