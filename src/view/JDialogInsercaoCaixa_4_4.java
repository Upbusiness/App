    private void fnRegisterAddValueCashier() {

        String code_main = JTextFieldTools.enterCodeAcsses(this);
        if (ClassUser.validatorCode(code_main)) {

            new Sons().tocarFim();
            System.err.println("Validou!!");

            if (ClassCashier.newOpening(Double.parseDouble(text_valor_usado_retirada.getText().replace(",", ".")),
                    jComboBoxDescricaoRetirada.getSelectedItem().toString(), 0, Integer.parseInt(text_turno_retirada_caixa.getText()),
                    Integer.parseInt(text_terminal_retirada_caixa.getText()))) {

                cpc.printUserCashier(2, BeanLogin.getUser(), jComboBoxDescricaoRetirada.getModel().getSelectedItem().toString(), text_valor_usado_retirada.getText(), idCashier, ClassCodRandon.codeGeneratorOpenCashier(), headerCoupon);

                pc.openDrawer();
                pc.actionGuillotine();

                JOptionPane.showMessageDialog(null, "Suprimento lançado com sucesso.\n Clique em 'OK' para sair.", "Aviso", JOptionPane.INFORMATION_MESSAGE);

                JDialogInsercaoCaixa_4_4.this.dispose();
            }

        } else {

            JOptionPane.showMessageDialog(this, "Codigo inválido!", "UPMarket diz:", JOptionPane.ERROR_MESSAGE);
            new Sons().tocarErro();

        }
    }
