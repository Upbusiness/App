    private void fnLogin() {

        try {
            usuario = login_usuario.getText();
            senha = login_senha.getText();
            controle = ClassUser.loginUser(usuario, senha);

            if (controle) {
                jLabel4.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icons/[010972].png"))); // NOI18N  
                isLocked = false;

                if (!Bean.isIsOpened()) {
                    
                   
                    Bean.setIsLoged(true);
                    Bean.setIsOpened(true);
                    this.dispose();
                    new JFrameSale_1().show();
                    //new JFrameAdministrator().show();

                } else {

                    this.dispose();

                }

            } else {

                isLocked = true;
                jLabel4.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icons/[010970].png"))); // NOI18N
                //JOptionPane.showMessageDialog(null, "Usu치rio ou senha inv치lido", "Erro de Login", JOptionPane.WARNING_MESSAGE);
                login_usuario.setText("");
                login_senha.setText("");
                login_usuario.requestFocus(true);
                jLabel6.setVisible(true);

            }

        } catch (HeadlessException ex) {
            System.err.println("ERROR::" + ex);
            //newXML.generateLog(ex.toString());
            System.err.println(ex.getMessage());
        }
    }

    private void fnOpenURL(URI uri) {
        if (Desktop.isDesktopSupported()) {

            Desktop desktop = Desktop.getDesktop();
            try {
                desktop.browse(uri);

            } catch (IOException ex) {
                System.err.println("ERROR::" + ex);
                //newXML.generateLog(ex.toString());
            }
        } else {
            System.err.println("Erro de compatibilidade.");
        }

    }
